# O operador % retorna o resto da divisão de um número pelo outro 

print(14 % 4)

# COMO FUNCIONA?
#  14 // 4 dá 3  → este é o quociente inteiro;
#  3 * 4   dá 12 → como resultado da multiplicação de quociente e divisor;
#  14 - 12 dá 2  → este é o restante.

print("Exemplo um pouco mais complicado: ")
print(12 % 4.5)
# 12 // 4.5 dá 2.0,
# 2.0 * 4.5 dá 9.0,
# 12 - 9.0  dá 3.0.
