# O operador ** em Python é usado para realizar a exponenciação. 
# Em outras palavras, ele eleva um número a uma determinada potência
# para melhor visualização  BASE ** EXPOENTE
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# O ponto após o numero quer dizer que ele é um numero de ponto flutuante(float) em vez de um inteiro(int)