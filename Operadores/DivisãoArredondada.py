# O sinal da barra dupla // é o operador para divisão inteira
print(6 // 3)   # Saída: 2
print(6 // 3.)  # Saída 2.0
print(6. // 3)  # Saída 2.0
print(6. // 3.) # Saída 2.0

# A divisão de inteiro por inteiro dá um resultado inteiro
print("Outros Exemplos")
print(6 // 4)
print(6. // 4)
print(-6 // 4)
print(6. // -4)