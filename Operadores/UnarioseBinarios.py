# Operador unário e binário
print(-4 - 4)   # Saída: -8 (unário: -4, binário: -)

# Operador binário com float
print(4. - 8)   # Saída: -4.0 (binário: -)

# Operador unário com float
print(-1.1)     # Saída: -1.1 (unário: -)

# Operador unário de identidade
print(+2)       # Saída: 2 (unário: +)
print(+2)       # Saída: 2 (unário: +)
print(+2)       # Saída: 2 (unário: +)
